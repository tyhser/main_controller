/* External variables --------------------------------------------------------*/
extern TIM_HandleTypeDef htim5;
extern DMA_HandleTypeDef hdma_usart1_tx;
extern UART_HandleTypeDef huart1;

uint8_t Rx_Cnt,Rx_Mark,Notarize,Mark;
extern uint8_t Rx_Data[14],Board_Number;
extern volatile uint32_t Counter_1,Counter_2,Counter_3,Counter_4;				//电机控制变量


void USART1_IRQHandler(void)
{
	huart1.RxXferCount=14;
	if (__HAL_UART_GET_FLAG(&huart1,UART_FLAG_RXNE)) {
		uint8_t temp2 = USART1->DR;
		if( (0x55==temp2) && (Rx_Cnt==0) )
			Rx_Mark=1;
				
		if( (1==Rx_Mark) && (0xAA==temp2) && (Rx_Cnt==0) )
			Notarize=1;
		
		if(Notarize==1)
		{
			Rx_Cnt++;				
			Rx_Data[Rx_Cnt]=temp2;
		}
		
		if( (Rx_Cnt==13) && (Rx_Data[12]==0x5A) && (Rx_Data[13]==0xA5) )
		{
			if(Board_Number==Rx_Data[2])
			{
                /*判断功能序号*/
				if( ( Rx_Data[3]==1 && Counter_1==0 ) || ( Rx_Data[3]==2 && Counter_2==0 ) || ( Rx_Data[3]==3 && Counter_3==0 ) || 
					  ( Rx_Data[3]==4 && Counter_4==0 ) || ( (5<=Rx_Data[3])&&(Rx_Data[3]<=15) ) )
					Mark=1;
			}
			Rx_Cnt=0;
			Rx_Mark=0;
			Notarize=0;
			Rx_Data[0]=0x55;
		}
		else if(Rx_Cnt>=13)
		{
			Rx_Cnt=0;
			Rx_Mark=0;
			Notarize=0;			
		}
		else if( (Rx_Cnt<13) && (Rx_Data[Rx_Cnt-1]==0x5A) && (Rx_Data[Rx_Cnt]==0xA5) )
		{
			Rx_Cnt=0;
			Rx_Mark=0;
			Notarize=0;						
		}
	}
  HAL_UART_IRQHandler(&huart1);
}

