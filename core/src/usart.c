void usart3_enable(uint8_t rx_enable, uint8_t tx_enable)
{
    if (rx_enable) {
        SET_BIT(USART3->CR1, UART_MODE_RX);
    } else {
        CLEAR_BIT(USART3->CR1, UART_MODE_RX);
    }
    if (tx_enable) {
        SET_BIT(USART3->CR1, UART_MODE_TX);
    } else {
        CLEAR_BIT(USART3->CR1, UART_MODE_TX);
    }
}
